/**
 * ============================================================================================
 * ОСНОВНАЯ КОНФИГУРАЦИЯ EXPORT DEFAULT
 * ============================================================================================
 * 
 * defineConfig() - функция, которая создает объект конфигурации Playwright
 * Все настройки ниже будут применены ко всем тестам в проекте
 */
export default defineConfig({
  // testDir - директория, где находятся тестовые файлы
  // './tests' - относительный путь от корня проекта
  // Playwright будет искать все файлы с расширением .spec.ts в этой папке
  testDir: './tests',

  // fullyParallel - запускать ли тесты в файлах параллельно
  // true - да, тесты в разных файлах будут выполняться одновременно
  // false - нет, файлы будут выполняться по очереди
  // Параллельный запуск ускоряет общее время тестирования
  fullyParallel: true,

  // forbidOnly - запретить ли оставлять test.only в коде при запуске в CI
  // !!process.env.CI - преобразует значение переменной окружения CI в булево значение
  // process.env.CI - переменная, которая устанавливается системами непрерывной интеграции
  // (GitHub Actions, GitLab CI, Jenkins и т.д.)
  // true - если запустить тест с .only (только этот тест) в CI, сборка упадет
  // Это предотвращает случайные коммиты с test.only
  forbidOnly: !!process.env.CI,

  // retries - количество повторных попыток при падении теста
  // process.env.CI ? 2 : 0 - тернарный оператор:
  //   - если запущено в CI (process.env.CI существует) -> 2 попытки
  //   - если запущено локально -> 0 попыток
  // Полезно в CI для flaky тестов (нестабильных тестов)
  retries: process.env.CI ? 2 : 0,

  // workers - количество параллельных процессов (воркеров) для запуска тестов
  // process.env.CI ? 1 : undefined - тернарный оператор:
  //   - в CI запускать по 1 воркеру (меньше нагрузка на систему)
  //   - локально использовать все доступные ядра процессора (undefined)
  // workers: 1 в CI предотвращает конфликты и нестабильность
  workers: process.env.CI ? 1 : undefined,

  // reporter - формат отчетов о результатах тестирования
  // 'html' - генерирует HTML-отчет, который можно открыть в браузере
  // Отчет будет доступен в папке playwright-report после запуска тестов
  // Другие варианты: 'list' (текстовый), 'json', 'junit', 'allure'
  reporter: 'html',

  // use - общие настройки для всех тестов
  // Эти настройки применяются к каждому тесту по умолчанию
  use: {
    // baseURL - базовый URL для действий page.goto() без полного URL
    // Пример: await page.goto('/about') откроет http://localhost:3000/about
    // Закомментировано, так как тесты используют полные URL
    baseURL: 'https://godlike.host',

    // trace - настройка записи трассировки при падении теста
    // 'on-first-retry' - записывать трассировку только при первой попытке
    // Трассировка - это запись всех действий браузера (скриншоты, сетевые запросы)
    // Очень полезно для отладки упавших тестов
    // Можно посмотреть в Trace Viewer: npx playwright show-trace
    trace: 'on-first-retry',

    // viewport - размер окна браузера по умолчанию
    // width: 1800, height: 900 - разрешение 1920x1080 (Full HD) с небольшим запасом
    // Можно переопределить в отдельных тестах через test.use({...})
    viewport: {
      width: 1800, height: 900,
    },

    // deviceScaleFactor - плотность пикселей (DPR - Device Pixel Ratio)
    // 1 - стандартный монитор (1 пиксель CSS = 1 физический пиксель)
    // 2 - Retina/High-DPI дисплеи (1 пиксель CSS = 2 физических пикселя)
    // Влияет на скриншоты и некоторые визуальные проверки
    deviceScaleFactor: 1,
  },

  // projects - конфигурация проектов (браузеров) для тестирования
  // Каждый проект = отдельный браузер с определенными настройками
  // Тесты будут запущены во всех указанных браузерах
  projects: [
    // Проект 1: Google Chrome (Chromium)
    // Имитирует десктопный Chrome на компьютере
    // ...devices['Desktop Chrome'] - копирует все настройки из предустановки Desktop Chrome
    {
      name: 'chromium',
      use: { ...devices['Desktop Chrome'] },
    },

    // Проект 2: Mozilla Firefox
    // Имитирует десктопный Firefox
    {
      name: 'firefox',
      use: { ...devices['Desktop Firefox'] },
    },

    // Проект 3: Apple Safari (WebKit)
    // Имимитирует десктопный Safari на Mac
    {
      name: 'webkit',
      use: { ...devices['Desktop Safari'] },
    },

    /**
     * ============================================================================================
     * ЗАКОММЕНТИРОВАННЫЕ ПРОЕКТЫ (МОБИЛЬНЫЕ УСТРОЙСТВА)
     * ============================================================================================
     * 
     * Можно раскомментировать для тестирования на мобильных устройствах:
     */
    // {
    //   // Google Chrome на мобильном устройстве (Pixel 5)
    //   name: 'Mobile Chrome',
    //   use: { ...devices['Pixel 5'] },
    // },
    // {
    //   // Safari на iPhone 12
    //   name: 'Mobile Safari',
    //   use: { ...devices['iPhone 12'] },
    // },

    /**
     * ============================================================================================
     * ЗАКОММЕНТИРОВАННЫЕ ПРОЕКТЫ (ДРУГИЕ БРАУЗЕРЫ)
     * ============================================================================================
     * 
     * Можно добавить для тестирования в других браузерах:
     */
    // {
    //   // Microsoft Edge
    //   name: 'Microsoft Edge',
    //   use: { ...devices['Desktop Edge'], channel: 'msedge' },
    // },
    // {
    //   // Google Chrome (если установлен отдельно)
    //   name: 'Google Chrome',
    //   use: { ...devices['Desktop Chrome'], channel: 'chrome' },
    // },
  ],

  // webServer - запуск локального веб-сервера перед тестами
  // Полезно для тестирования локальной версии приложения (localhost)
  // Playwright дождется запуска сервера перед началом тестов
  // 
  // Закомментировано, так как в данном проекте тестируется готовый сайт (godlike.host)
  // Если бы тестировали локальное приложение, нужно было бы раскомментировать:
  // webServer: {
  //   command: 'npm run start',  // команда для запуска сервера (из package.json)
  //   url: 'http://localhost:3000',  // URL, по которому доступен сервер
  //   // reuseExistingServer - переиспользовать ли уже запущенный сервер
  //   // true - да, если сервер уже запущен (не запускать новый)
  //   // false - нет, всегда запускать новый (для чистоты в CI)
  //   reuseExistingServer: !process.env.CI,
  // },
});

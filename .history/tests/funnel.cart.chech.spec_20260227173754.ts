/**
 * ============================================================================================
 * АВТОТЕСТЫ PLAYWRIGHT - ТЕСТИРОВАНИЕ ВОРОНКИ ПОКУПКИ GODLIKE.HOST
 * ============================================================================================
 *
 * Основные понятия:
 * - test() - определяет тестовый сценарий
 * - expect() - проверяет условия (assertions)
 * - page - объект, представляющий веб-страницу в браузере
 * - await - ожидание выполнения асинхронной операции
 *
 * Зависимости:
 * - @playwright/test (см. package.json)
 * - playwright.config.ts - конфигурация тестов
 * ============================================================================================
 */

// Импортируем 'test' и 'expect' из библиотеки Playwright
// test - используется для определения тестовых сценариев (каждый test() = один тест)
// expect - используется для проверки assertions (утверждений о состоянии элементов/страницы)
// Зависимость: @playwright/test (указана в package.json в секции devDependencies)
import { test, expect } from "@playwright/test";

/**
 * ============================================================================================
 * КОНФИГУРАЦИЯ ТЕСТА (test.use)
 * ============================================================================================
 *
 * Настройки в этом блоке применяются ко всем тестам в данном файле.
 * Они переопределяют глобальные настройки из playwright.config.ts для этих тестов.
 *
 * Зачем нужно: чтобы обеспечить одинаковые условия для всех тестов в файле.
 * Это гарантирует, что тесты будут работать предсказуемо.
 */
test.use({
  // viewport - определяет размеры окна браузера при выполнении теста
  // width: 1800, height: 900 - ширина и высота окна в пикселях
  // Большой размер экрана позволяет видеть больше элементов и избежать проблем с адаптивностью
  // (некоторые элементы могут быть скрыты на маленьких экранах)
  viewport: {
    width: 1800,
    height: 900,
  },
  // deviceScaleFactor - определяет плотность пикселей устройства
  // 1 - стандартная плотность (обычные мониторы)
  // 2 - Retina дисплеи (экраны Apple с высокой плотностью пикселей)
  // Это влияет на то, как элементы отображаются и тестируются
  deviceScaleFactor: 1,
});

/**
 * ============================================================================================
 * ТЕСТ 1: test base funnel - Основной тест воронки продаж
 * ============================================================================================
 * 
 * Название: "test base funnel" (тест базовой воронки)
 * Назначение: Проверка полного пути пользователя от главной страницы до оформления заказа
 * 
 * Что тестируется (пошагово):
 * 1. Посещение главной страницы сайта godlike.host
 * 2. Переход на страницу выбора тарифа Minecraft сервера
 * 3. Добавление выбранного тарифа в корзину
 * 4. Авторизация (вход в существующий аккаунт)
 * 5. Выбор локации (расположения) сервера
 * 6. Переход на страницу финальной проверки заказа (Review & Checkout)
 * 
 * @param {page} - объект страницы Playwright, предочески
 *                 через механизм dependency injection Playwright
ставляется автомати *                 используется для взаимодействия с веб-страницей (клики, ввод текста, проверки)
 * 
 * Как запустить этот тест:
 * npx playwright test tests/funnel.spec.ts:test base funnel
 * или просто: npx playwright test (запустит все тесты)
 */
test("test Credit/Debit Card (Stripe", async ({ page }) => {
  // ШАГ 1: Открытие главной страницы сайта godlike.host
  // page.goto() - метод для перехода по указанному URL
  // 'https://godlike.host' - адрес тестируемого сайта (хостинг-провайдер для игровых серверов)
  // await - ожидает, пока страница полностью загрузится, перед переходом к следующему шагу
  await page.goto("https://godlike.host");

  // ШАГ 2: Поиск и клик по ссылке "Minecraft Server Hosting" в шапке сайта
  // page.getByRole('banner') - ищет элемент с ролью 'banner' (это header/шапка сайта)
  // getByRole ищет элементы по их ARIA роли, что является хорошей практикой accessibility
  // .getByRole('link', {name: 'Minecraft Server Hosting'}) - внутри баннера ищет ссылку с текстом "Minecraft Server Hosting"
  // {name: ...} - атрибут для поиска по видимому тексту или accessible name
  // .click() - выполняет клик левой кнопкой мыши по найденной ссылке
  // Это действие перенаправляет пользователя на страницу выбора тарифов Minecraft хостинга
  await page
    .getByRole("banner")
    .getByRole("link", { name: "Minecraft Server Hosting" })
    .click();

  // ПРОВЕРКА 1: Проверка, что URL изменился на страницу с тарифами Minecraft
  // expect(page).toHaveURL() - assertion (утверждение), проверяющее текущий URL страницы
  // /minecraft-java-servers-hosting/i - регулярное выражение (RegExp):
  //   - 'minecraft-java-servers-hosting' - искомая часть URL
  //   - 'i' - флаг, делающий поиск нечувствительным к регистру (i = insensitive)
  // Тест провалится (упадет), если URL не содержит указанную строку
  // Это подтверждает, что мы действительно перешли на страницу Minecraft тарифов
  await expect(page).toHaveURL(/minecraft-java-servers-hosting/i);

  // ШАГ 3: Поиск кнопки "Add to Cart" (Добавить в корзину)
  // page.getByText('Add to Cart') - ищет любой элемент, содержащий текст "Add to Cart"
  // Это может быть кнопка, ссылка или другой элемент с этим текстом
  // .first() - выбирает первый найденный элемент (на случай, если их несколько на странице)
  // Мы получаем ссылку (локатор) на кнопку, но ПОКА НЕ кликаем по ней
  const orderButton = page.getByText("Add to Cart").first();

  // ПРОВЕРКА 2: Проверка видимости кнопки "Add to Cart" перед кликом
  // .toBeVisible() - assertion, проверяющий что элемент видим на странице
  // (не скрыт CSS, не удален из DOM, не имеет display:none и т.д.)
  // timeout: 10000 - ожидание до 10 секунд, пока элемент не станет видимым
  // Это важно, так как кнопка может загружаться динамически (AJAX, lazy loading)
  // Если кнопка не появится за 10 секунд - тест упадет с ошибкой
  await expect(orderButton).toBeVisible({ timeout: 10000 });

  // ШАГ 4: Клик по кнопке "Add to Cart" - добавление товара в корзину
  // После этого действия пользователь должен быть автоматически перенаправлен в корзину
  // (или появится уведомление о добавлении, зависит от реализации сайта)
  await orderButton.click();

  // ПРОВЕРКА 3: Проверка, что URL изменился на страницу корзины
  // \/cart\/? - регулярное выражение для URL корзины:
  //   - '\/' - экранированный слеш (в URL используется /)
  //   - 'cart' - слово cart
  //   - '\/?' - необязательный слеш в конце (/cart или /cart/)
  // Тест проверяет, что мы находимся на странице /cart или /cart/
  await expect(page).toHaveURL(/\/cart\/?/);

  // ШАГ 5: Поиск формы авторизации (входа в аккаунт)
  // page.locator('.auth-block__form') - поиск элемента по CSS селектору
  // '.auth-block__form' - класс элемента формы авторизации (форма входа)
  // locator используется, когда нет встроенных методов типа getByRole или getByText
  // Обычно на странице корзины, если пользователь не авторизован, показывается форма входа
  const authForm = page.locator(".auth-block__form");

  // ПРОВЕРКА 4: Проверка видимости формы авторизации
  // Убеждаемся, что форма входа отображается на странице корзины
  // Это логично - неавторизованный пользователь должен войти перед оформлением
  await expect(authForm).toBeVisible();

  // ШАГ 6: Переключение на вкладку/кнопку "Login" (Вход)
  // На странице корзины обычно есть переключатель между:
  // - "Login" (Вход) - для тех, у кого уже есть аккаунт
  // - "Register" (Регистрация) - для новых пользователей
  // По умолчанию может быть выбрана регистрация, поэтому переключаемся на вход
  // .getByText('Login') - ищет элемент с текстом "Login"
  // .click() - выполняет клик для переключения на форму входа
  await page.getByText("Login").click();

  // ШАГ 7: Заполнение формы авторизации (вход в аккаунт)
  // Нам нужно заполнить два поля: Email и Password, затем нажать кнопку входа

  // page.getByRole('textbox', { name: '* Email' }) - ищет текстовое поле по роли 'textbox'
  // { name: '* Email' } - ищет поле по placeholder, label или accessible name, содержащему "Email"
  // Символ '*' в начале часто означает обязательное поле (required) - это часть названия поля
  // .fill() - очищает поле и вводит указанный текст

  // Вводим email пользователя (тестовый аккаунт)
  await page
    .getByRole("textbox", { name: "* Email" })
    .fill("test@testmail.com");

  // Вводим пароль пользователя
  // Поле ищется по name: '* Password' (обязательное поле пароля)
  await page
    .getByRole("textbox", { name: "* Password" })
    .fill("test@testmail.com");

  // Ищем кнопку "Login" по роли 'button' и тексту 'Login'
  // .click() - выполняем клик для отправки формы авторизации
  // После этого система должна проверить credentials и войти в аккаунт
  await page.getByRole("button", { name: "Login" }).click();

  // ШАГ 8: Переход на следующий шаг (выбор локации сервера)
  // После успешной авторизации пользователь перенаправляется на страницу настройки заказа
  // Там нужно будет выбрать различные параметры: локацию, количество слотов и т.д.
  // Нажимаем кнопку "Next step" (Следующий шаг) для перехода к выбору локации
  await page.getByRole("button", { name: "Next step" }).click();

  // ПРОВЕРКА 5: Проверка заголовка "Choose location" (Выберите локацию)
  // page.getByRole('heading', { name: 'Choose location' }) - ищет заголовок (h1-h6) с текстом "Choose location"
  // getByRole('heading') - ищет элементы с ролью heading (любого уровня h1-h6)
  // Заголовок подтверждает, что мы перешли на этап выбора локации сервера
  // Примечание: здесь нет await expect().toBeVisible(), поэтому проверка может быть нестрогой
  // (это скорее "подтверждение", чем обязательная проверка)
  await expect(
    page.getByRole("heading", { name: "Choose location" }),
  ).toBeVisible();

  // ШАГ 9: Повторный клик по "Next step" для подтверждения выбора локации
  // После выбора локации (или оставления значений по умолчанию) переходим к следующему шагу
  // Обычно на этом этапе выбирается локация сервера (например, Germany, USA, Russia)
  await page.getByRole("button", { name: "Next step" }).click();

  // ПРОВЕРКА 6: Проверка финального URL после прохождения всех шагов настройки
  // После выбора локации пользователь должен оказаться на странице оформления заказа
  // Страница checkout - это последний этап перед оплатой
  await expect(page).toHaveURL(
    "https://godlike.host/clientarea/cart.php?a=checkout",
  );

  // ШАГ 10: Поиск заголовка "Review & Checkout" (Проверка и оформление)
  // page.getByRole('heading', { name: 'Review & Checkout' }) - ищет заголовок страницы
  // Это последний этап воронки - страница с обзором заказа перед оплатой
  // Здесь пользователь может проверить свой заказ: выбранный тариф, локацию, цену
  const reviewHeading = page.getByRole("heading", {
    name: "Review & Checkout",
  });

  // ПРОВЕРКА 7: Финальная проверка - заголовок "Review & Checkout" должен быть видим
  // Это подтверждает, что весь путь воронки пройден успешно
  // Если мы видим этот заголовок - заказ оформлен и готов к оплате
  await expect(reviewHeading).toBeVisible();

  // ШАГ 11: Не применять кредит
  await page
    .locator("label")
    .filter({ hasText: "Do not apply any credit from" })
    .getByRole("insertion")
    .click();

  // ШАГ 12: Выбор Credit/Debit Card (Stripe)
  await page
    .locator("label")
    .filter({ hasText: "Credit/Debit Card (Stripe)" })
    .getByRole("insertion")
    .click();

  // ШАГ 13: Проверяем, что появились iframe полей карты

  const cardNumberFrame = page.frameLocator(
    'iframe[name*="__privateStripeFrame7553"]',
  );
  const expiryFrame = page.frameLocator('iframe[name*="exp-date"]');
  const cvvFrame = page.frameLocator('iframe[name*="cvc]');

  // Проверяем, что поля отображаются
  await expect(cardNumberFrame.locator("input")).toBeVisible();
  await expect(expiryFrame.locator("input")).toBeVisible();
  await expect(cvvFrame.locator("input")).toBeVisible();

  // ШАГ 14: Валидация ввода данных

  // 1. Проверка что поле номера принимает только цифры
  await cardNumberFrame.locator("input").fill("abcd1234");
  await expect(cardNumberFrame.locator("input")).toHaveValue("1234");

  // 2. Проверка длины номера карты (16 цифр)
  await cardNumberFrame.locator("input").fill("4111111111111111");
  await expect(cardNumberFrame.locator("input")).toHaveValue(
    "4111111111111111",
  );

  // 3. Проверка срока действия (MM/YY)
  await expiryFrame.locator("input").fill("1228");
  await expect(expiryFrame.locator("input")).toHaveValue(/12/);

  // 4. Проверка CVV (3 цифры)
  await cvvFrame.locator("input").fill("12");
  await expect(cvvFrame.locator("input")).toHaveValue("12");

  await cvvFrame.locator("input").fill("123");
  await expect(cvvFrame.locator("input")).toHaveValue("123");
});
